// 都道府県名から市区町村リストを取得
export function getCitiesByPrefecture(prefectureName: string): string[] {
  const prefecture = prefectures.find((p) => p.name === prefectureName);
  return prefecture ? prefecture.cities : [];
}

// すべての都道府県名（定数として事前に計算）
export const ALL_PREFECTURES = prefectures.map((p) => p.name);

// すべての都道府県名を取得（互換性のため関数も残す）
export function getAllPrefectures(): string[] {
  return ALL_PREFECTURES;
}
